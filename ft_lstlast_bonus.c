#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*p;

	if (!lst)
		return (NULL);
	p = lst;
	while (p)
	{
		if (p-> next == NULL)
			return (p);
		p = p-> next;
	}
	return (p);
}
